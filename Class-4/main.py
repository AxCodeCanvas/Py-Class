"""
>>> Arithmetic Operator <<<

1. Addition (+)
2. Subtraction (-)
3. Divide (/)
4. Multiplication (*)
5. Reminder (%)
6. Exponent (**) (base ^ power)
7. Floor Division
"""

num1 = 10
num2 = 20

print(num1 + num2)  # 30
print(num1 - num2)  # -10
print(num1 * num2)  # 200
print(num2 / num1)  # 2.0
print(num2 % 3)  # 2
print(2 ** 2)  # 4
print(num2 // 19)  # 1
