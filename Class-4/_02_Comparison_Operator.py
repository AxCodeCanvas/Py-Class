"""
-> Outcome - True / False

1. Equality (==)
2. Not Equal (!=)
3. Less than or Equal (<=)
4. Less than (<)
5. Greater than or Equal (>=)
6. Greater than (>)

"""

num1 = 100
num2 = 200

print(num1 == num2)
print(num1 != num2)
print(num1 <= num2)
print(num1 < num2)
print(num1 >= num2)
print(num1 > num2)
